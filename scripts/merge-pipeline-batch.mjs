function buildProfile(item, meta) {
  const aboutUrl = meta.aboutUrl || meta.website;
  const profile = {
    slug: item.slug,
    name: item.name,
    category: item.category,
    tagline: meta.tagline,
    description: meta.description,
    website: meta.website,
    hq: meta.hq,
    domains: meta.domains,
    tags: meta.tags,
    onsitePolicy: "Hybrid — verify on careers page",
    lastVerified: TODAY,
    verificationStatus: "verified",
    sources: [
      { label: "About page", url: aboutUrl },
      ...(meta.careersUrl ? [{ label: "Careers", url: meta.careersUrl }] : []),
      ...(meta.locationsUrl ? [{ label: "Office locations", url: meta.locationsUrl }] : []),
    ],
  };

  if (meta.founded) profile.founded = meta.founded;
  if (meta.officeCities) profile.officeCities = meta.officeCities;
  if (meta.officeCountries) profile.officeCountries = meta.officeCountries;
  if (meta.totalOfficeLocations) profile.totalOfficeLocations = meta.totalOfficeLocations;
  if (meta.locationsUrl) profile.locationsUrl = meta.locationsUrl;
  if (meta.contactUrl) profile.contactUrl = meta.contactUrl;
  if (meta.careersUrl) profile.careersUrl = meta.careersUrl;
  if (meta.linkedin) profile.linkedin = meta.linkedin;
  if (meta.products?.length) profile.products = meta.products;
  if (meta.services?.length) profile.services = meta.services;
  if (meta.leadership?.length) profile.leadership = meta.leadership;

  return profile;
}

function main() {
  const catalogPath = join(ROOT, "data", "companies.json");
  const pipelinePath = join(ROOT, "data", "pipeline.json");

  const catalog = JSON.parse(readFileSync(catalogPath, "utf8"));
  const pipeline = JSON.parse(readFileSync(pipelinePath, "utf8"));
  const pipelineItems = [...pipeline.inProgress, ...pipeline.unverified];

  const existingSlugs = new Set(catalog.companies.map((c) => c.slug));
  const added = [];
  const skipped = [];
  const missing = [];

  for (const item of pipelineItems) {
    if (existingSlugs.has(item.slug)) {
      skipped.push(item.slug);
      continue;
    }
    const meta = META[item.slug];
    if (!meta) {
      missing.push(item.slug);
      continue;
    }
    catalog.companies.push(buildProfile(item, meta));
    existingSlugs.add(item.slug);
    added.push(item.slug);
  }

  catalog.catalogUpdated = TODAY;

  writeFileSync(catalogPath, JSON.stringify(catalog, null, 2) + "\n");

  pipeline.inProgress = [];
  pipeline.unverified = [];
  writeFileSync(pipelinePath, JSON.stringify(pipeline, null, 2) + "\n");

  console.log(`Pipeline items processed: ${pipelineItems.length}`);
  console.log(`Added to catalog: ${added.length}`);
  if (skipped.length) console.log(`Skipped (already in catalog): ${skipped.join(", ")}`);
  if (missing.length) {
    console.error(`Missing META for: ${missing.join(", ")}`);
    process.exit(1);
  }
  console.log(`Catalog total: ${catalog.companies.length} companies`);
  console.log(`Pipeline cleared: inProgress=${pipeline.inProgress.length}, unverified=${pipeline.unverified.length}`);
}

main();
